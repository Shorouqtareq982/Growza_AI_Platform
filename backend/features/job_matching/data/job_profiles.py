# ==================== HELPER FUNCTIONS ====================

def get_all_skills_from_all_jobs() -> list:
    """Get ALL skills from ALL job profiles"""
    all_skills = []
    for profile in JOB_PROFILES.values():
        all_skills.extend(profile.get("skills", []))
    return list(set(all_skills))

def get_all_tools_from_all_jobs() -> list:
    """Get ALL tools from ALL job profiles"""
    all_tools = []
    for profile in JOB_PROFILES.values():
        all_tools.extend(profile.get("tools", []))
    return list(set(all_tools))

def get_job_profile(job_title: str) -> dict:
    """Get complete profile for a job title"""
    return JOB_PROFILES.get(job_title, None)


def get_skills_for_job(job_title: str) -> list:
    """Get all skills for a job (list of strings)"""
    profile = get_job_profile(job_title)
    if not profile:
        return []
    return profile.get("skills", [])


def get_tools_for_job(job_title: str) -> list:
    """Get all tools for a job (list of strings)"""
    profile = get_job_profile(job_title)
    if not profile:
        return []
    return profile.get("tools", [])


def get_all_job_titles() -> list:
    """Get list of all job titles in profiles"""
    return list(JOB_PROFILES.keys())
